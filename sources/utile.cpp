void sort(Cartes *tab,int nb)
{
  bool tab_en_ordre = false;
  int taille = nb;
  while(!tab_en_ordre)
  {
    tab_en_ordre = true;
    for(int i=0;i<nb-1;i++)
    {
      if(tab[i]>tab[i+1])
      {
        swap(tab[i],tab[i+1]);
        tab_en_ordre = false;
      }
    }
    taille--;
  }
}
